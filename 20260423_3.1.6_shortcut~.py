#4.2.5 リストから新たなリストを生成する：リスト内包表記
'''
リスト内包表記は、処理の「手順（How）」を順番に書くのではなく、
「最終的に欲しいデータの形（What）」をシステムに宣言して、
裏側の処理を丸投げするためのトップダウン型の構文
[ ] （外枠）も大事：システムに対し「最終成果物はリストである」と宣言する。
中身のルール：そのリストの中に「どういうルールでデータを詰めるか」を記述する。

for は「命令」ではなく「前置詞（〜について）」
英語の語順（"I want i*2, for every i in data"）
（私は i * 2 という結果が欲しい。data の中に入っているそれぞれの i について）や、
数学の集合の記法（ {2x∣x∈D} ）をプログラミングの構文にそのまま落とし込んだ形

◆【アーキテクチャ】リスト内包表記が「速い」本当の理由
「最終的に欲しいデータの形（What）を宣言して、裏側の処理を丸投げする」解釈は10000%大正解です。
そして、この書き方はコードが美しくなるだけでなく「for と append() を使った通常のループよりも、
実行速度が圧倒的に速い」**というインフラエンジニアにとって最大のメリットがあります。
●なぜ速いのか？
通常の append() ループ: 現場監督はループを1周するたびに「data2 の append というメソッドを探して、
それを実行して…」というPythonレベルの解釈処理を毎回行わなければならず、オーバーヘッド
（見えない無駄な処理）が発生します。
リスト内包表記: 「結果はリストになる」と最初に宣言（[]）されているため、
現場監督は「あ、これリスト作るだけね！」と判断し、Pythonの解釈をスキップして、
裏側にあるC言語の極めて高速なレイヤーで一気にリストを構築してしまうのです。
実務において、数万行に及ぶルーターのログデータから「エラー行だけを抽出する」
ような処理を書く場合、リスト内包表記（[line for line in logs if 'ERROR' in line]）を使うだけで、
処理時間が劇的に短縮されます。
'''
data = [15,43,7,59,98]
data2 = [i*2 for i in data]
print(data2)
    #[30, 86, 14, 118, 196]
data2 = [str(i) for i in data]
print(data2)
    #['15', '43', '7', '59', '98']
data = [15,43,7,59,98]
data2 = []
for i in data:
    data2.append(i*2)
print(data2)
    #[30, 86, 14, 118, 196]

