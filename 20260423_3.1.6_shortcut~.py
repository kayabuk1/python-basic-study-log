def test_score_calc():
    # データを一箇所で定義する（これ以降、手打ちしない）
    subjects = ['国語', '算数', '英語', '理科', '社会']
    test_score_lists = []
    
    # 1. 入力処理を for 文でまとめる
    for subject in subjects:
        print(f'{subject}の点数を入力：')
        test_score_lists.append(float(input()))

    num_of_subjects = len(test_score_lists)
    average_score = sum(test_score_lists) / num_of_subjects
    
    print('\n【入力データ確認】')
    # 2. 出力処理も for 文と zip() 関数でまとめる
    # subjectsとtest_score_listsから、要素を1つずつペアにして取り出す
    for subject, score in zip(subjects, test_score_lists):
        print(f"{subject}：{score:.0f}")

    print(f"合計点は{sum(test_score_lists):.0f}点です。")
    print(f"平均点は{average_score:.1f}です。")

test_score_calc()

