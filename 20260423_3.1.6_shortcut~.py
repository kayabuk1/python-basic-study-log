#4.3.1 ループを中断する：break命令
sum = 0
for i in range (1,101):
    sum += i
    if sum > 1000:
        break
print(F'合計が1000を超えるのは、1～{i}を加算した時です。')
    #合計が1000を超えるのは、1～45を加算した時です。

#4.3.2 現在の周回をスキップする：continue命令
#ex:2の倍数だけ全部足し合わせる
sum = 0
for i in range(1,101):
    if i % 2 != 0:
        continue
    sum += i
print(F"合計値は{sum}です。")
    #合計値は2550です。
'''◆「ガード節（早期リターン・早期コンティニュー）」**というアーキテクチャ設計
もし「変数が50より大きく、かつ文字列で、かつ○○の場合だけ処理する」というように
if の中に if をネスト（入れ子）していくと、インデントがどんどん右にずれていき、
処理の全貌を把握するのが難しくなります。
これを防ぐために「もし条件を満たしていなければ、さっさと関数を抜ける（return）、
あるいはループの次へ行く（continue）」という「関所（ガード）」を
コードの最初に設ける手法が推奨されます。
# ガード節の例
for item in data:
    if not item:     # もし中身が空っぽ（偽）なら
        continue     # 以下の処理はせず、すぐ次の要素へ行け！（ガード）
    # ここから下は「絶対に中身が入っている」と確信して処理を書ける
    print(item)
このように、コードを書く際に「最初に除外条件をすべて弾いてしまう（防弾装甲を張る）」ことで、
メインの処理を安心してインデントの浅い場所で書けるようになります。
'''

