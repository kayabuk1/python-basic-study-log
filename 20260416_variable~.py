'''
f (Formatted string) ➔ 【変数展開】 {name} のように変数を文字列の中に埋め込める。
r (Raw string) ➔ 【エスケープ無効化】 \n や \\ などのバックスラッシュを特殊文字として扱わず、
   そのままの文字として認識する。
b (Bytes literal) ➔ 【バイト列生成】 文字列（str）ではなく、ネットワーク通信や
   バイナリファイル用の「バイト列（bytesオブジェクト：0〜255の数値の配列）」を生み出す。
t (Template string) ➔ 【テンプレート文字列】 これは Python 3.14で追加されたばかりの最新機能 です。
   f-stringに似ていますが、セキュリティや独自の解釈ルールを持たせるための高度なテンプレート機能です。

▲fb や ft といった組み合わせは、システム上 物理的に不可能です！
f, b, t の3つは、**「最終的にどんな材質のオブジェクト（製品）を作るのか」**を
決めるメインスキルです（文字列を作るのか、バイト列を作るのか、テンプレートを作るのか）。
だから同時に2つを指定するとシステムが「どっちを作ればいいんだ！」とパニックになります。
'''


