'''
 print(id(a) is id(b)) の裏側で何が起きたか
① まず aと bの関係（おさらい） 
   aの中身は 2 、 bの中身も 2 です。
   「2」はシステムが最初から用意して使い回している「小さな整数（-5〜256）」なので、
  実は a も bも、メモリ上の全く同じ「2」の箱を指しています。
② id() 関数が実行される id(a) を実行すると、システムは「2」の箱の住所（メモリアドレス）を調べます。
　これは例えば 140722332243752 のようなものすごく大きな数字になります。
③ システムが「新しい箱」を作る システムは、調べ出した 140722332243752 という
　大きな数字を画面に表示したり比較したりするために、この大きな数字を入れるための
　 「新しい整数の箱」をメモリ上に新規作成します。
  同じように、右辺の id(b) を実行したときにも、値自体は同じ 140722332243752 ですが、
  システムはそれを入れるためのもう一つの「新しい整数の箱」を別で新規作成します。
④ 比較演算子の出番
  id(a) == id(b) の場合：
  「中身の数字は同じか？」と聞かれているので、両方とも 140722332243752 だから True を返します。
id(a) is id(b) の場合：
 「右と左は、全く同じ箱か？」と聞かれています。中身の数字は同じですが、
 ③の通り、システムが関数を実行した都度、別々の「大きな数字の箱」を作ってしまっているため、
 箱としては別物と判定され False になります。
'''

