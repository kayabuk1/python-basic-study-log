#%% 8.4 関数の呼び出しと戻り値
# 8.4.1 複数の戻り値
def get_max_min(*args):
    return(max(args), min(args))
    #Q.return max(args), min(args)と丸かっこを外しても良いのはなぜ？
    #A. システム（パーサ）は、2つの値の間にある カンマ , を見つけた瞬間に
    #、「おっ、カンマで区切られているな！ということは、これはこのまま2つセットにして『タプル
    #()』にパッキングして出荷（return）しろという命令だな！」 と自動的に解釈してくれます
    #Q.max,minはどんな組み込み関数だっけ？
max_v, min_v = get_max_min(17,7,5,108,-10)
#複数の引数を受け取り、 １つのタプルにまとめる。
print(max_v)
    #108
print(min_v)
    #-10
    
#名前付きタプルを生成する namedtuple関数
#名前付きタプル（namedtuple）を使う最大の意味は、
#**「データの書き換えを防ぐ『安全なカプセル（イミュータブル）』としての性質を
#保ったまま、辞書（dict）のように『ドット（名前）で分かりやすく
#データを取り出せる』ようにするため」**です。
import collections
def get_max_min(*args):
    MaxMin = collections.namedtuple('MaxMin', ['max','min'])
    return MaxMin(max(args), min(args))
result = get_max_min(15,7.5,108,-10)
print(result.max)
    #108
print(result.min)
    #-10
print(result[0])
    #108

#%% 8.4.2 再帰関数
def factorial(num):
    if num != 0:
        return num * factorial(num-1)
    return 1
print(factorial(5))
    #120
    
#%% 8.4.3 高階関数 （Higher-order）
#Q.高階とはどんな意味？
#A. 「高階（こうかい）」とは、数学や計算機科学の用語で、
#**「関数自体を引数として受け取ったり、戻り値として返したりする関数」**のこと
#Pythonの世界では**「関数は第一級オブジェクト」
#なので他の数値型などと同じく、 関数の引数として渡したり、
#戻り値として返す事ができる。

#高階関数walk_list関数を定義
def walk_list(data, func):
    #システムの動き: walk_list という名前の工場（関数）の設計図を作ります。
    #この工場の入り口には、処理対象のデータを受け取る data スロット
    #と、「処理手順が書かれた別の工場の設計図」を受け取るための
    # func 仮引数（パラメータ）が用意されます。これが高階関数の入り口です。
    #Q. func:期待されるデータ型ヒントを記載する場合は？
    #標準ライブラリの typing モジュールにある Callable
    #（コーラブル：呼び出し可能オブジェクト） という型を使います
    #Q.具体的にはどう記述する？ 
    #リストの内容を順に処理
    for key, value in enumerate(data):
        #enumerate関数＝列挙関数
        #Q.数字をキーに辞書を作ってくれる？
        #辞書（dict）を作るわけではありません。「インデックス番号と
        #要素」がセットになった「タプル ()」をループのたびに作り出しています
        #k1周目: (0, 105)
        #2周目: (1, 53) そして、このタプルが for key, value 
        #によってアンパック（展開）され、key に 0、value に 105 が
        #代入されているのです。
        #func経由で指定の関数を呼び出し
        func(value, key)
        #システムは「func 名札が貼られた『別の関数オブジェクト』を今ここで起動
        #() させろ！」と解釈し、その入り口に value と key を材料として
        #投げ込みます。
        #システムが「あ、ここに関数が来たんだな」と判断して実行に踏み切るのは、
        # 冒頭の定義を見た時ではなく、関数の中盤にある以下のコードに
        # 到達した**「実行の瞬間」**です。
        # func(value, key)
        # ここでシステムは、func という名札の後に ()（丸括弧） が
        # 付いているのを発見します。 
        # 公式ドキュメント（6.3.3. 呼び出し）には、オブジェクトの後ろに
        # () をつけると、そのオブジェクトを「呼び出し可能オブジェクト
        # （callable）」として呼び出す（起動する）と定義されています
        #Q.func名札が貼られた関数はどれ？
#リストを処理するためのユーザ定義関数
def show_item(value, key):
    print(key, ':', value)
data = [105,53,27,87,33]
walk_list(data, show_item)
#もし show_item() と書いてしまうと「関数を実行した結果」を渡すことになりますが、
#() を付けずに show_item とだけ書くことで、システムに対して
#**「この関数の実体（工場の設計図）そのものを、そのまま
# func スロットに届けてくれ！」**と命令しているのです。
#walk_list(data, show_item) と呼び出された瞬間、
#グローバル空間にいた show_item という工場（の実体）が、
#walk_list 工場の中に運び込まれます。
#そして、その工場内だけで通用する func という一時的な名札に
#貼り替えられている のです。名前が変わっただけで、
#実体は間違いなく show_item です。
#リストを順番に取り出すという共通の仕組み（フレームワーク）」
#と、「取り出した値を使って何をするかという個別のアクション（関数）」**
#を完全に分離できます。
#Q.関数は何らかの処理を行う工場のようなものだと思っていた。
#   ここでは処理を利用しないということ？
#A.今すぐ工場を稼働（実行）させるのではなく、工場の設計図（実体）
#のまま別の場所へ運び、後で好きなタイミングで稼働させる」**ということです。
#walk_list 工場の内部の func(value, key) という行に来て
#初めてカッコ () が付けられ、「よし、運んできたこの工場を、今ここでこの
#材料を使って稼働させろ！」と処理が実行される
'''
0 : 105
1 : 53
2 : 27
3 : 87
4 : 33
'''

#%% 8.4.4 無名関数（ラムダ式）　lambda シンプルな関数を式として表現する
#ラムダ式は新しい機能ではなく、さっき学んだ高階関数をさらにサボって書く
#（DRY原則）ためのただの省略記法
'''
【さっきのコード（defを使う場合）】
# 1. わざわざ show_item という名前の工場を作る
def show_item(value, key):
    print(key, ':', value)

# 2. 骨組みに、その工場を名前で指定して差し込む
walk_list(data, show_item)
しかし、エンジニアはこう考えます。 「この show_item という工場、ここでしか使わないのに、
わざわざ def で名前をつけてグローバル空間に登録するの、
面倒くさいし名前がもったいないな……。名前をつけずに、
 カセットの中身（処理）だけをその場で直接差し込めないか？」
これを実現するのが lambda（無名関数）
'''
data = [105,53,27,87,33]
walk_list(data,
          lambda value,key : print(key,':',value))
            #lambda 仮引数リスト : 戻り値となる式（処理）
#今回のコード lambda value, key : print(key, ':', value) を
#パーサ（構文解析器）の気持ちになって読み解いてみます。
#lambda ➔ パーサ：「おっ、名前の無い使い捨て工場が今ここから作られるんだな」
#value, key ➔ パーサ：「この工場には value と key という2つの材料
#受け入れスロットがあるんだな」
# : ➔ パーサ：「ここから右側が、工場の中身の処理だな」
#print(key, ':', value) 
#➔ パーサ：「渡された材料を使って print を実行するんだな。
#そして、その結果を（ return を書かなくても自動的に）外に放り投げるんだな」
#※lambda は「式」であるため、関数の時のように
# return と書く必要すらありません（書くと構文エラーになります）。
#システムが気を利かせて、 : の右側の結果を自動的にリターンしてくれます。
'''
0 : 105
1 : 53
2 : 27
3 : 87
4 : 33
'''

