#1．変数に代入する。
calcu = lambda num : num * 2
print(calcu(10))
    #20
    #この場合、 代入した変数名が関数名となる。（ただしそれならdefを使うべき）
    #あくまで「ラムダ式は変数にも代入できる（第一級オブジェクトである）」ことを示す
    #ための文法テスト用サンプル

#2.引数を持たないラムダ式
import datetime
now = lambda: datetime.date.today()
print(now())
    #2026-05-26
    #Q.これわざわざ lambdaを使う理由は？？？
    #ただ今日の日付を表示したいだけなら
    #print(datetime.date.today()) と直接書けば済む話です。
    #では、実務においていつこの「引数なしラムダ」を使うのでしょうか？
    #答えは、「実行のタイミングを遅延させる（後回しにする）ため」です。
    #❌ ダメな渡し方（カッコあり）： 
    # button( action = datetime.date.today() ) 
    # カッコが付いているため、ボタンを作ったその瞬間に時間が確定してしまい、
    # 明日ボタンを押しても「昨日の日付」が出力されてしまいます。
    #⭕ 正しい渡し方（引数なしラムダのカセット）：
    # button( action = lambda: datetime.date.today() )
    #「引数なしで実行できるカセット」として渡すことで、ボタンが押されるまで実行を保留し、
    #押された瞬間に初めて最新の日付を取りに行くことができます。
    #このように、「外部からデータ（引数）を受け取る必要はないけれど、
    #実行タイミングだけを相手の骨組みに委ねたい」という時に、
    #この引数なしラムダが強力な「遅延実行カセット」として活躍するのです。

