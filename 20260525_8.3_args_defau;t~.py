# *関数定義という専用の看板を描くための「特殊な構文ルール（デフォルト引数値）」 
def get_triangle(base:int or float = 5, height:int or float = 1) \
    -> int or float:
        return base * height /2
        # def f(ham: str, eggs: str = 'eggs') -> str:」と明記されています
        #。 つまり、パーサ（構文解析器）は
        #「①引数名 ➔ ②型ヒント（:） ➔ ③デフォルト値（=）」 という順番で
        #解読するように厳格に設計されています。
        #Pythonのパーサ（構文解析器）は、関数定義の引数を読み取るとき、
        #非常に厳格な順番で解釈を進めます。
        #base ➔ パーサ：「よし、まずは base という空っぽの名札（仮引数）を用意したぞ」
        # : ➔ パーサ：「おっ、コロンが来たな。ということは、ここから先はシステムを動かす
        #コードではなく、**『人間やIDE（VS Codeなど）に向けたアノテーション（メモ）』**だな。
        #メモを名札の裏に書き込んでおこう」
        # = ➔ パーサ：「イコールが来たぞ！ここから先はメモではなく、
        #**『デフォルト値として箱に入れる実体（中身）』**だな。名札にデータを結びつけよう」
        #このように、 : と = はパーサにとって「今から何を読み取るか」を切り替える
        #重要なスイッチ（デリミタ）です
        
        # int or float の正体: 以前の学習である「ブール演算の短絡評価」を
        #思い出してください。式 A or B は、左側の A が真であれば、
        #右側を見ずに A をそのまま返します
        #Pythonでは int（クラスオブジェクト）自体も 「真」 として評価されるため、
        #実行時に int or float が計算されると、システムは単に int を
        #返してしまいます。 つまり、設計者としては「整数または小数」とメモを残したつもりでも、
        #システムやVS CodeなどのIDEからは**「この引数は絶対に整数（int）しか
        #受け付けない！」と誤認されてしまう**のです。
        
        #正しい型の和（Union）の書き方:
        # 「AまたはB」という型を指定したい場合、現在のPython（3.10以降）では
        # |（パイプ）記号を使った新しい型ユニオン演算子 を使用するのがプロの鉄則です
        #正しくは int | float と書かなければなりません。
        #int or float は int に化けるのか？
        #順番を正しく base: int or float = 5 と書いたとします。
        #構文エラーにはなりませんが、**「型ヒントとしては完全にバグっている」**状態になります。
        #Pythonでは、アノテーション（メモ）の部分に書かれた内容も、
        #関数が定義された瞬間に 「式」 として実行（評価）されます。
        #ここで、公式ドキュメント（6.11 ブール演算）に記載されている、
        # or 演算子の**「短絡評価（ショートサーキット）」**の絶対ルールを思い出してください。
        #式 x or y は、まず x を評価します; x が真なら x の値を返します;
        # それ以外の場合には、y を評価した結果値を返します。」
        #では、システムが int or float という式を計算する時の動きをトレースしてみましょう。
        #左側の int を調べる:
        #int というのは数値ではなく「整数クラスというオブジェクトの
        #実体」です。Pythonでは、空っぽのリストなど一部の例外を除き、クラスオブジェクト
        #そのものは常に**「真 (True)」**として扱われます。
        #短絡評価が発動する:
        #左側の int が「真」であると確定した瞬間、システムは「左がTrueなら、
        #右側が何であっても or の結果はTrueだ！なら右側（float）なんて見る必要は
        #ない、左側の int をそのまま結果として放り投げよう！」と判断します
        #その結果、設計者が int or float（整数か小数のどちらか）というメモを
        #残したつもりでも、システムが読み取った瞬間に短絡評価によって float が
        #消し飛び、ただの int（整数のみ）というメモに書き換えられてしまうのです。
        #これが or の正体と罠です。
'''
--------------------------------------------------------------------------------
第3部：【アーキテクチャの進化】なぜ |（パイプ）が正解なのか？
この or の問題を解決するため、Python 3.10で**「新しい型ユニオン演算子（PEP 604）」**
というアーキテクチャが導入されました
。
本来、| （パイプ）という記号は、数値に対して使う「ビット単位のOR演算子（2進数の計算）」です
（例：1 | 2）。しかし、Pythonの設計者たちはこの記号に**全く新しい裏の顔
（オーバーロード）**を与えました。
【システムの裏側の動き】 
 | 演算子の左右に数値ではなく「型（クラスオブジェクト）」が置かれた場合、
 システムはビット計算の工場をキャンセルし、**「ユニオン（和集合）型作成工場」**を起動させます。
# システムの認識
int | float
システム：「左が int 型、右が float 型だな。よし、この2つをガッチャンコして
 、『intまたはfloatのどちらでも受け入れる』という新しい1つの保護カプセル
 （Union型オブジェクト）を作ろう！」
or の場合: 単なる論理計算なので、左側だけを見て int を残して終了してしまう（短絡評価）
。
| の場合: 「型の合成」という特別な処理が呼び出されるため、
左右両方の型をきちんと認識して int | float という「複数の型を許容する
正しいメモ」が完成する。
--------------------------------------------------------------------------------
■ 統合結論（インフラエンジニアのメンタルモデル）
昌敏さん、これがシステムの裏側で起きている決定的な違いです。
順番の真理: パーサは記号でモードを切り替える。: はメモの開始、= は実体の代入の開始。
or の真理: プログラムの実行をコントロールするための「論理演算子」。
処理をサボる（短絡評価）性質があるため、型ヒントに使うと右側が無視されてバグる
。
| の真理: クラスの間に置かれた時だけ「型を合成する」という特殊能力を発揮する、
型ヒント専用の最新の演算子
。「人間の直感的な言葉の『or（または）』」と、「システムが厳密に処理する
『or（論理演算子）』」の間に生じた解釈のズレ。
これをアーキテクチャのレベルで修正したのが | 記号の導入なのです。
'''
print(F'三角形の面積は{get_triangle():.2}です')
    #三角形の面積は2.5です

