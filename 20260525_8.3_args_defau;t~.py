#最後の arg3 にだけ 300 を渡そう！」
#そして、省略したつもりで以下のように呼び出しました。
my_func(100, 300)
#システムの裏側の動き（大いなる勘違い）】 システムは、
#人間が何を省略したかったのか（忖度）を一切考えず、公式ドキュメントの絶対ルール
#**「N 個の位置引数がある場合、位置引数は先頭の N スロットに配置されます」**
#に従って愚直に処理します。
#1つ目のデータ 100 が来たぞ！ ➔ よし、一番左の arg1 スロットに入れよう。
#2つ目のデータ 300 が来たぞ！ ➔ よし、左から2番目の arg2 スロットに入れよう。
#（🚨 ここでズレが発生！）
