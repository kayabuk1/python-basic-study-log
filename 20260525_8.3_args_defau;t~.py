#【結果の出力】
#arg1=100, arg2=300, arg3=0
#人間は「arg2を省略して、arg3に300を入れた」つもりだったのに、
#システムは左から順番に詰めていく性質上、「arg2に300を入れて、
#arg3を省略した」と解釈してしまったのです。
#これが、「途中で省略（デフォルト値）されると、順番が狂ってシステムが迷う
#（人間の意図を誤認する）」という現象の具体的な正体です。

