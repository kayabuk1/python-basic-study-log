'''
システムの裏側の動き（引数のスロット割り当て）:
    ある引数がデフォルト値を持っている場合、それ以降 " * " が出てくるまでの
    引数は全てデフォルト値を持っていなければなりません -- これは文法定義では
    表現されていない構文的制限です。
     (a, key=b, c, *d) はダメ、(a, key=b, *c, d) なら書ける
関数にデータを渡すとき、システムは基本的に「左から順番にスロットにはめ込んでいく（位置引数）」と
いう話をしました。 この「順番にはめ込む」という処理をしている間は、途中に
「省略可能な穴（デフォルト値）」があると、後続のデータがズレてしまってシステムがパニックを
起こすため、「一度デフォルト値を設定したら、後ろも全てデフォルト値にしなければならない」と
いう厳格なルールが存在します
。
では、関数定義の中に * が単独で現れると、システムはどう動くのでしょうか？
公式ドキュメントには、* を配置することで「キーワード専用として引数をマークする」
と定義されています。 これはシステムの裏側の動きで見ると、
「システムよ、ここから先は『左から順番にスロットへはめ込む作業（位置引数）』を完全に終了せよ！」
という強力な強制停止（デリミタ）の合図なのです。
第2部：【解剖】 なぜ「キーワード専用」だと制限が解除されるのか？
* によって「左から順番に流し込むモード」が終了させられると、
それより右側に書かれた引数（例えば height）には、
もう順番でデータを入れてあげることができなくなります。
では、どうやって height にデータを渡すのか？
 「名前（キーワード）を直接指定して、ピンポイントでデータを送り込む
 （例：height=10）」しか方法がなくなるのです。
 これが「キーワード専用引数」に切り替わるメカニズムです

 *args （変数名あり） ＝ モードを強制終了しつつ、余った位置データをタプルとして**「回収」**する。
* （単独） ＝ データ回収はしないが、
    モードの強制終了（デリミタ）だけを**「発動」**させ、以降の引数を絶対にキーワード指定させる。
'''
#関数の引数に *（アスタリスク）を単独で置くと、システムに対して
# 「ここから後ろの引数は、左からの順番で入れるのではなく、
#必ず名前（キーワード）を指定して渡せ」と強制するモード（キーワード専用引数）に切り替わります
#これを使えば、デフォルト値の制約を受けずにコードを書くことができます。
# * 以降は「必ず名前を指定して渡す引数」になる
def get_triangle(base=5, *, height):
    return base * height / 2
# 呼び出し時は必ず `height=` と名前を指定するため、システムも迷わない！
print(get_triangle(height=10)) 
    #25.0
# もし height= を省略すると
print(get_triangle(10)) 
    #TypeError: get_triangle() missing 1 
    #required keyword-only argument: 'height'

def my_func(arg1, *, arg2=0, arg3=0):
    pass
my_func(1,2,3)
    #TypeError: my_func() takes 1 positional argument 
    #but 3 were given
    #型エラー： my_func() 工場は、左から順番に放り込まれる材料（位置引数）
    #を1つ（arg1）しか受け取らない設計になっているのに、あなたは今回、
    #順番頼みで3つ（1と2と3）も材料を放り込んできましたよ！というエラー

#第2部：【シミュレーション】 もし * がなかったら、どう狂うのか？
#では、ここからが本題です。 もし、この関数から * を取り除き、位置引数のまま
# 「途中の引数を省略」しようとした場合、システムがどう勘違いをして狂っていくのかを見てみましょう。
#【危険な設計図（* なし）】
def my_func(arg1, arg2=0, arg3=0):
    print(f"arg1={arg1}, arg2={arg2}, arg3={arg3}")
