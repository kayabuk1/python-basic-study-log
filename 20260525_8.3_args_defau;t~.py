#第3部：【アーキテクトの視点】 なぜ * が防弾装甲になるのか
#第2部のような「エラーは出ないが、データが間違った場所に入って計算が進んでしまう状態」を、
#インフラの世界では サイレントエラー（沈黙のバグ） と呼び、最も恐れられています。
#エラーが出ないため、誰も気づかないまま間違った設定が本番環境に適用されてしまうからです。
#だからこそ、提示したコードの *（キーワード専用引数のマーカー） が最強の防弾装甲として
#機能するのです。
def my_func(arg1, *, arg2=0, arg3=0):
    pass
#この設計図にしておけば、先ほどの「arg2 を省略して arg3 にだけデータを渡したい」という場合、
#人間は強制的に以下のように書かざるを得なくなります。
#my_func(100, arg3=300)
#こう書けば、システムは「なるほど、100は左端の arg1 に入れて、arg3 
#という宛先が書いてある荷物は確実に arg3 スロットに届ける。
#残った arg2 はデフォルト値を使うんだな！」と100%絶対に迷わなくなります。
#そして、もし人間がサボって my_func(1, 2, 3) や my_func(100, 300) 
#のように順番だけで渡そうとすれば、システムは冒頭の TypeError を出して
#**「名前を書けって言っただろ！」と実行前に安全にクラッシュさせてくれる**のです。

#%% 8.3.3 可変長引数の関数　仮引数の頭に * を付ける
def total_products(*values):
    #仮引数名の前に * をつけると、システムに対して
    #**「外部から送られてきた『名前のないデータ（位置引数）』を
    #全てかき集めて、1つの変更不可能な『タプル ( )
    #』というカプセルにパッキングしろ！」**という命令（可変長位置引数）になります
    result = 1
    #足し算の合計を出すなら 0 スタートですが、
    #今回は「掛け算（乗算）」の合計を出すからです。
    #もし result = 0 でスタートしてしまうと、何を掛けてもずっと 0 になってしまい、
    #バグ（論理エラー）になります。
    for value in values:
        result *= value
        #乗算して、そのまま上書き（代入）する
    return result
print(total_products(12,15,-1))
    #-180
    #呼び出し時：total_products(12, 15, -1) と
    #呼ばれると、システムは (12, 15, -1) というタプルを作成し、
    #values という名札を貼ります。
    #すべての掛け算が終わり、最終的に 180 という実体（オブジェクト）を
    #関数の外へと放り投げます。そして、個室（ローカル名前空間）は爆破されます。
print(total_products(5,7,8,2,1,15))
    #8400

'''
第1部：【設計図を描く側（関数定義）】の用語
これらは、def を使って工場のルールを決める際に使われる用語です。
1. 仮引数（Parameter: パラメータ）
    意味: 工場の入り口に用意しておく「空っぽの名札」の総称です
    。
    例: def my_func(a, b): の a と b のこと。
    本質: 外部からデータが流れてきたときに、とりあえず貼り付けるための
    仮の名前（シンボル）です。
    この後の 3つ は、すべて「仮引数の種類や状態」を表す言葉にすぎません。
2. 必須引数（Required argument）
    意味: 「この材料がないと工場が動かないから、絶対に渡してくれ！」
    というルールの仮引数です。
    例: 前回学んだ def get_triangle(base=5, *, height):
    における height のこと。
    本質: デフォルト値（規定値）が設定されていないため、
    呼び出し時にデータ渡しを省略すると、
    システムが TypeError でクラッシュして安全に止まってくれます。
3. 任意引数（Optional argument / デフォルト引数）
    意味: 「基本は備え付けの材料を使うけど、別の指定があればそっちを使うよ」
    という、省略可能な仮引数です
    。
    例: def get_triangle(base=5): の base のこと。
    本質: 設計時に = を使ってデフォルト値（予備のデータ）をセットした仮引数です
    。データ渡しが省略された時はその初期値が代入され、
    データが指定されれば上書きされます。
4. 可変長引数（Arbitrary arguments）
    意味: 「送られてきた材料を全部ひとまとめにして受け止めるブラックホール」の
    ような仮引数です
    例: *args（複数データをタプルとしてパッキング）や
    **kwargs（複数データを辞書としてパッキング）
    本質: 事前に数がわからないデータを柔軟に受け止めるための
    防弾装甲です。以前学んだ
    def total_products(*values): がまさにこれです。

--------------------------------------------------------------------------------
第2部：【工場を稼働させる側（関数呼び出し）】の用語
    これらは、定義された関数に対して「データをどうやって渡すか
    （実引数：Argument）」のアクションを表す用語です

5. 位置引数（Positional argument）
    意味: 名札（宛先）を指定せず、「左から順番にスロットにはめ込んでいく」渡し方です
。
    例: get_triangle(10, 20) の 10 と 20。
    本質: スロットの物理的な順番に依存するため、
    途中の引数を省略（デフォルト値を利用）しようとすると、
    後続のデータがズレてしまってシステムが迷う原因（サイレントエラー）になりやすい、
    原始的ですが最もよく使われる渡し方です。
6. キーワード引数（Keyword argument）
    意味: 順番は無視して、「このデータはこの名札（キー）に貼ってね」
    と宛先を明示する渡し方です
。
    例: get_triangle(height=20, base=10) 。
    本質: 名前=値 の形で渡すため、順番がどうなっていようがシステムは
    絶対に迷わず正しいスロットにデータを届けることができます。
    前回の関数定義で登場した * （キーワード専用引数）は、
    この「安全な渡し方」を呼び出し側に強制するための鉄の壁でした。

--------------------------------------------------------------------------------
■ 統合結論（アーキテクトのメンタルモデル）
昌敏さん、これらの用語をインフラエンジニアの実務（API設計やCLIコマンド）にマッピングするとこうなります。
仮引数: パラメータシートの「項目名」全体
必須引数: 絶対に入力が必要な「必須パラメータ」
任意引数: 空欄なら初期値が使われる「オプションパラメータ」
可変長引数: 複数のIPアドレスをカンマ区切りで無限に入れられる「動的リスト入力欄」
位置引数: CLIで ping 192.168.1.1 のように順番だけで認識させる渡し方
キーワード引数: CLIで ping -S 192.168.1.1 のようにフラグ（名前）を明記する確実な渡し方
'''

#%%　注意：想定される引数まで可変引数にまとめない
def concatenate(prefix, suffix, *args):
    #prefix: 左から1番目のデータが絶対に入る「必須引数」。
    #suffix: 左から2番目のデータが絶対に入る「必須引数」。
    #*args: それ以降に流れてきた「名前のない余ったデータ」を全て掬い取って、
    #1つのタプル（変更不可のカプセル）に圧縮パッキングする「可変長引数」
    result = prefix
        #prefix の中身である '[' という文字列オブジェクトを貼り付けます。
    result += '.'.join(args)
        #区切り文字 . に対して join を呼び出し、タプルの中身を
        #順番に連結します。これにより
        #'鈴木.エメルシア.富士子' という1つの新しい文字列が生み出されます。
        #result += ...: これは「累算代入文」です
        #現在の result の中身（'['）と、
        #先ほど作った文字列（'鈴木.エメルシア.富士子'）を
        # + 演算子で連結（足し算ではなく結合）し
        #、その結果である 
        # '[鈴木.エメルシア.富士子' で result 名札を上書きします。
    return result + suffix
        #現在の result （'[鈴木.エメルシア.富士子'）と、
        #suffix の中身（']'）を + で連結します。
        #完成した '[鈴木.エメルシア.富士子]' という
        #文字列オブジェクト（実体）を、工場の外にポンッと放り投げます。
        #そして工場の個室は爆破されます。
print(concatenate('[',']','鈴木','エメルシア','富士子'))
    #[鈴木.エメルシア.富士子]
    #システムの動き: 外部から5つのデータが位置引数（順番頼み）で投げ込まれます
    #1番目の '[' は、 prefix スロットに入ります。
    #2番目の ']' は、 suffix スロットに入ります。
    #残った '鈴木', 'エメルシア', '富士子' は、
    #まとめてブラックホール *args に吸い込まれ、
    #('鈴木', 'エメルシア', '富士子') という
    #1つのタプルオブジェクトに変換されます


#%% 可変長引数で[1個以上の引数]を表す方法

#%% 8.3.4 可変長引数（キーワード引数） **kwargs
#*args（タプルへのパッキング）と対をなす、 **kwargs（辞書へのパッキング）
def create_dict(**kwargs):
    result = dict()
    for key,value in kwargs.items():
        result[key] = value
    return result
d = create_dict(name='山田太郎',age=18, sex='male')
print(d)
    #{'name': '山田太郎', 'age': 18, 'sex': 'male'}

