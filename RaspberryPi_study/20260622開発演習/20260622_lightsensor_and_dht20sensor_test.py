import time
import smbus2
from grove.adc import ADC

# === 光センサーの設定（Grove Base Hatの A2 ポート） ===
# 水分センサーの時と同じく、ポート番号「2」だけを指定。
LIGHT_SENSOR_PIN = 2
adc = ADC()

# === DHT20 温湿度センサーの設定（I2C） ===
# I2Cバス1を使用。DHT20の固定アドレスは 0x38 です
 #⇒I2Cバス1を使えば良いというのはどの資料から分かったのだっけ？
 #⇒固定アドレスはどの資料から分かったのだっけ？
bus = smbus2.SMBus(1)
DHT20_ADDR = 0x38

print("DHT20および光センサーの測定を開始します（Ctrl+Cで終了）...")

try:
    while True:
        # ----------------------------------------------------
        # 1. 光センサーのデータ取得（アナログ）
        # ----------------------------------------------------
        light_value = adc.read(LIGHT_SENSOR_PIN)
        
        # ----------------------------------------------------
        # 2. DHT20のデータ取得（I2Cデジタル）
        # ----------------------------------------------------
        # 測定開始コマンドを送信
        bus.write_i2c_block_data(DHT20_ADDR, 0xAC, [0x33, 0x00])
         #●↑の測定開始コマンドがなぜこの16進数かというのはどこから分かったのだっけ？
         #●ソース資料などはある？
         #データシート2.Wait 10ms to send the 0xAC command (trigger measurement).
         # This command parameter has two bytes, thefirst byte is 0x33,
         # and the second byte is 0x00. 
         #訳：10ms待機し、0xACコマンド（⇒測定開始の命令）を送信してください。
         #このコマンドのパラメータ（⇒命令に付随する設定値）は2バイトあり、
         #1バイト目は0x33、2バイト目は0x00です。
         #●測定開始がwriteなのはなぜだっけ？
         #⇒マスターからスレイブに測定開始命令を書き込む必要があるため。
        time.sleep(0.1) # 測定完了まで待機
        
        # 生データ（7バイト）を読み出す
        data = bus.read_i2c_block_data(DHT20_ADDR, 0x71, 7)
         #生データが7バイトだと言う根拠は?
         #どこから0x71という数字が分かったの？
         #⇒現在の状態（1バイト）」と「湿度と温度の計測結果（各2.5バイトずつ）」
         #そして「CRC（チェック用の1バイト）」の合計7バイトを一度に吸い上げる。
        print(data)
        
        # ビット演算で生データを翻訳
        humi_raw = ((data[ 1 ] << 12) | (data[ 2 ] << 4) | (data[ 3 ] >> 4))
        temp_raw = (((data[ 3 ] & 0x0F) << 16) | (data[ 4 ] << 8) | data[ 5 ])
        print(humi_raw)
        print(temp_raw)
        #●ビット演算の動作についても詳しく解説してほしい。
        #⇒1番目の引き出しの8桁を、左に12個ズラします。これで20桁のうち、一番上の8桁（13〜20桁目）に配置されます。
        #⇒ 2番目の引き出しの8桁を、左に4個ズラします。これで真ん中の8桁（5〜12桁目）に配置されます。
        #⇒右に4個ズラす（はみ出た温度のデータは捨てる）**ことで、純粋な「湿度の下4桁（1〜4桁目）」を取り出します。
        #| (OR演算: 論理和) 上記で位置を合わせた3つの部品を、「OR演算（足し算のようなもの）」で1つの20桁の数値に結合（ガッチャンコ）します。
        
        # パーセントと摂氏（℃）に変換
        humi = round((humi_raw / 1048576.0) * 100.0, 2)
        temp = round((temp_raw / 1048576.0) * 200.0 - 50.0, 2)
         #1048576.0という数字はどこから出て来たのだっけ？
         #⇒データシート8 signalConversionの変換公式より2**20
        # ----------------------------------------------------
        # 3. ターミナルへ出力（レーダー表示）
        # ----------------------------------------------------
        print(f"光センサー: {light_value} | 温度: {temp} ℃ | 湿度: {humi} %")
        
        time.sleep(1)

except KeyboardInterrupt:
#●前のGPIOの時ははfinallyだったと思うけれど、今回はexceptを使っているのはなぜ？
#⇒プログラムが止まる前に必ず GPIO.cleanup() を実行してピンの占有
#（電流の出力状態など）を解放（後始末）しなければならなかったため、
#finally が必須だった。
    print("\n測定を終了します。")
