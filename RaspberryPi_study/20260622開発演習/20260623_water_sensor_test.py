import RPi.GPIO as GPIO
import time

# Grove Base HatのD16ポートに接続
WATER_SENSOR_PIN = 16

# BCMモードと、ピンの入力（IN）設定
GPIO.setmode(GPIO.BCM)
GPIO.setup(WATER_SENSOR_PIN, GPIO.IN)

print("水センサーの監視を開始します（Ctrl+Cで終了）...")

try:
    while True:
        # D16ピンのデジタル状態（HIGH or LOW）を読み取る
        sensor_state = GPIO.input(WATER_SENSOR_PIN)
        
        # 水に濡れているとLOW(0)、乾いているとHIGH(1)になる
        #※水に濡れることでGNDと短絡して電圧がGNDに落ちてLowになる。
        if sensor_state == GPIO.LOW:
            print("【正常】 水あり：ポンプ駆動可能です。")
        else:
            print("【警告】 水切れ！！：モーター空回りの危険があります！")
            
        time.sleep(1)

except KeyboardInterrupt:
    print("\n水センサーのテストを終了します。")

finally:
    # インフラの絶対の掟（防弾装甲）
    print("ピンの占有を解放します。")
    GPIO.cleanup()

