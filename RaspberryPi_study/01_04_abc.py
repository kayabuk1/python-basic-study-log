from grove.adc import ADC
from time import sleep

#VOL_PIN = 0
adc = ADC()
#print(type(adc))
#print(adc)
#<class 'grove.adc.ADC'>
#<grove.adc.ADC object at 0x7fb63d7a10>
#このオブジェクトabcは一体どんなオブジェクトなのだ？
#こいつにA2ピンの操作をメソッドとしてさせても良いのか？
#新たにabcオブジェクトを生成しなくて良いのだろうか？
#でもそれはなぜ？
'''
adc = ADC() という実体は、「Raspberry Piから見て、Grove Base Hatの
	上に載っている**アナログ・デジタル変換用マイコン（STM32チップ）
	**そのものとI2C通信を行うための専用の窓口（セッション）」です。
なぜ使い回せるのか？： 資料のハードウェア仕様を見ると、
	このチップは「12s-bit 8 channel」のADCであると記載されて
	います
	つまり、**「1つのマイコンチップが、A0〜A7までの8個の
	入り口（チャンネル）をすべて一人で監視・管理している」
	**というアーキテクチャになっています。 
	そのため、Python側で窓口（オブジェクト）をわざわざ2つ作る
	必要はなく、「1つの窓口に対して、引数で『0番（A0）の値を
	教えて』『次は2番（A2）の値を教えて』と依頼（メソッドを実行）
	する」だけで、マイコンが適切に切り替えて返事をしてくれる設計
	になっているのです。
'''
#LIGHT_PIN = 2
#なぜPINを指定する時は大文字？
'''
Pythonの公式ドキュメントや教育資料（Hands-On Python）にも
 「定数の名前はすべて大文字にするのが慣習である
 （names for constants are all capital letters）」 
 と明確に定義されています
'''
sensor_configs = [
    ("A0ボリューム", 0),
    ("A2光センサー", 2)
]
#「センサーの表示名」と「ポート番号（定数）」の2つのデータを、
#**変更不可能なペアである「タプル ( )」としてまとめ、
#それらを複数格納できる箱である「リスト [ ]」**に格納しています。
#今後アナログセンサーが増えた場合は、この sensor_configs 
#のリストに ("新しいセンサー", ピン番号) と1行書き足すだけで
#済むようになります。

while True:
	results = list() 
	#組み込み関数であるlist() と呼び出すと、システムに対して
	#「新しく、中身が空っぽのリスト型オブジェクトをメモリ上に作成
	#（コンストラクト）しろ」という命令になります
	for name, pin in sensor_configs:
	#先ほど作った設定リストから、センサーのタプルを1つずつ
	#（1周目は ("A0ボリューム", 0)、2周目は ("A2光センサー", 2)）取り出します。
		val = adc.read(pin)
		#現在取り出している pin（1周目は 0、2周目は 2）を 
		#adc.read() メソッドに渡し、その瞬間のアナログ電圧をデジタル数値に変換して、
		#変数 val に格納します。
		results.append(f"{name}: {val}") 
		# リストに結果を追加 [7]
		#f"{name}: {val}" という「f文字列」の機能を使って、
		#「A0ボリューム: 673」という1つの文字列データを作成します。
		#そして、append メソッドを使って、先ほど空にしておいた
		# results リストの末尾に、その文字列を追加（アペンド）します。
	# 3. リストの中身を " | " で連結して1行で美しく表示 [8]
	print(" | ".join(results))
	#文字列の join メソッドを使い、リストの中の各文字列の間に
	# " | " という区切り文字を挟み込んで、1つの繋がった文字列
	#通常、関数は 関数名(データ) という形で書きますが、join は少し特殊で
	# 区切り文字.join(繋げたいリスト) という構文をとります
	#（A0ボリューム: 673 | A2光センサー: 13）に変換し、ターミナルに出力します。
	sleep(0.1)