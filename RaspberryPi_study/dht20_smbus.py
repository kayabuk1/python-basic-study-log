import time
import smbus2 # I2Cの配管を直接叩くための強力なモジュール [1]

# 1. インフラの準備：1番のI2C配管を開き、DHT20の内線番号（0x38）をロックオン
bus = smbus2.SMBus(1)
ADDR = 0x38

print("I2C直結でDHT20のハッキングを開始します...")
print("終了するには Ctrl+C を押してください。")

try:
    while True: # 半永久的に繰り返し実行する処理 [2]
        # 2. トリガー：センサーに対して「測定を開始しろ」という特殊コマンド（0xAC, 0x33, 0x00）を送信
        bus.write_i2c_block_data(ADDR, 0xAC, [0x33, 0x00])
        
        # 測定が終わるまで少し待つ（データシートの仕様で約80ミリ秒必要）
        time.sleep(0.1)
        
        # 3. データの吸い上げ：センサーから結果の「生データ（7バイト）」を読み出す
        # ※0x71はステータスを読み取るためのコマンド
        data = bus.read_i2c_block_data(ADDR, 0x71, 7)
      
        # 4. 翻訳（ビット演算）：機械語の生データを、シフト演算（<< や >>）でつなぎ合わせる
        # 湿度は 1番, 2番, 3番の上半分 に格納されている
        humi_raw = ((data[3] << 12) | (data[6] << 4) | (data[7] >> 4))
        
        # 温度は 3番の下半分, 4番, 5番 に格納されている
        temp_raw = (((data[7] & 0x0F) << 16) | (data[1] << 8) | data[2])

        
        # 復元した数値を、人間が読める「％」と「℃」に計算し直す（2の20乗 = 1048576 で割る）
        humi = (humi_raw / 1048576.0) * 100.0
        temp = (temp_raw / 1048576.0) * 200.0 - 50.0
        
        # 取得した値を表示
        print(f"温度: {temp:.1f} ℃,  湿度: {humi:.1f} %")
        
        # 1秒待機
        time.sleep(1)

except KeyboardInterrupt:
    # Ctrl+Cを押して中断する時の処理 [2]
    print("\nハッキングを安全に終了しました。")
