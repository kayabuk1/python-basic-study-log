#def check_scope2(): グローバルに工場を登録します。
#（呼び出し時） data = 'ローカル' （🚨 ここが超重要！） 
#   関数の中で =（代入）が行われました。Pythonには「宣言（int a;のような
#   事前の定義）」という文法がありません。そのため、公式ドキュメントには
#   **「関数の中で代入を行った場合、その名前は『新しいローカル変数』として
#   ローカルな名前空間に作成される」**という絶対ルールがあります
#   つまり、外側に同じ data という名前があっても上書き（破壊）はされず、
#   **関数の個室の中に「同姓同名の別人」の data が新しく作られ、外側の
#    data を一時的に隠してしまう（シャドウイング）**のです。
#return data システムは検索ルールに従い、まずローカル（個室）を探します。
#   すると先ほど作ったばかりの data があるので、外側のグローバルを見に行くことなく
#    'ローカル' を出荷します。
#   →関数実行後にreturn dataとなっている場合は、ローカル変数がまだ生き残ってしまう？
#   A.return が返すもの: ローカル変数という「箱」や「名前」を返すわけではありません。
#   計算して生み出された「オブジェクト（実体）」だけを裸のまま外に投げ出します。
#   関数の個室の運命: 投げ出した直後、個室の中にあるローカル変数（名札）は
#   一切の例外なくすべて消滅します。生き残ることは絶対にありません。
#print(data) 関数の外から data を呼び出します。
#   関数の個室はすでに消滅しており、グローバルの data は一切上書きされず
#   無傷で残っているため、'グローバル' が表示されます。


# より厳密な変数の有効範囲
num = 3
def check_scope3():
    print(num)
    #num = 108
    #UnboundLocalError: cannot access local variable 
    #'num' where it is not associated with a value
    #代入前にローカル変数numが参照されているといエラー
    return num
print(check_scope3())
print(num)
#　【なぜエラーになるのか？ コンパイラの深淵】
#もし num = 108 のコメントを外した場合、システムは以下のように動きます。
#num = 3 グローバルに num を登録。
#def check_scope3(): ここで、Pythonのパーサ（構文解析器）は
#   工場を登録する際、中身のコード全体をざっとスキャンして「変数の種類」を
#   事前に決定（静的解決）します
#   パーサは関数の中を見渡し、「おっ、下の方で num = 108 という代入をしているな！
#    ということは、この関数の中にいる num は、上から下まで全て『ローカル変数』として扱うぞ！
#   」とシステムに予約してしまいます
#（呼び出し時） print(num) 工場が稼働し、一番上の print(num) を
#   実行しようとします。システムは「この num はローカル変数だ（さっきそう決めた）
#   」と思っています。 しかし、実際の実行の流れでは、まだ num = 108 の行に
#   到達していません。 つまり、ローカル変数として予約されているのに、まだ値が紐付いて
#   いない（束縛されていない：Unbound）状態なのです。 その結果、「グローバルに
#    num=3 があるのは知っているが、この関数での num はローカル変数だと決まって
#   いる。しかしローカルにまだ値がない！」という矛盾が生じ、システムが 
#   UnboundLocalError: local variable 'num' referenced 
#   before assignment（ローカル変数 'num' が代入される前に参照されました） 
#   を送出してクラッシュします
#関数の中からどうしてもグローバル変数を書き換えたい場合は、関数の中で
#    global data のように「これは外側のヤツだよ」と明示的に宣言する
#   （global文）必要がありますが、これは依存関係が複雑になるため、
#   インフラのコードでは極力避けるべきとされています

#まとめ
#Pythonは純粋なインタプリタではなく、実行直前に『機械が読みやすい形
#（バイトコード）』へ高速コンパイルし、それを仮想マシンがインタプリト（逐次実行）する言語である」。
#だからこそ、実行前にコード全体を見渡して「ローカル変数の予約」などができ、
#その結果として前回の UnboundLocalError のような時間差のバグが生まれるのです。

#%% 関数を入れ子に定義した場合
data = 'global'
#1.システムは、一番外側の世界である「グローバル名前空間」に data という名札を作り、
#メモリ上の 'global' という文字列実体（オブジェクト）に貼り付けます。
def outer():
    #2.システムは「outer」という名前の巨大な工場（関数オブジェクト）の設計図を作成し、
    #グローバル名前空間に登録します。
    #※この時点では、パーサ（構文解析器）は中身をざっとスキャンして「この中には
    # data というローカル変数と、 inner という別の工場があるな」と予約するだけで、
    #中身のコード自体はまだ1ミリも実行しません。
    data = 'outer'
        #4.個室の中で =（代入）が行われたため、前回の絶対ルール「関数内で
        #代入を行うと新しいローカル変数が作られる」が発動します。グローバルの data 
        #は上書きされず、outer の個室内に「同姓同名の別人」である新しい名札 data 
        #が作られ、'outer' という文字列実体に貼られます（これをシャドウイング＝
        #隠蔽と呼びます）。
    def inner():
        data = 'inner'
        print(data,"in")
            #inner in
        return data
    print(data,"out")
        #outer out
    return inner()
#print(inner())
    #NameError: name 'inner' is not defined
print(outer())
    #inner
    #3.print(outer()) が実行され、いよいよ outer 工場の稼働スイッチが
    #押されます！outer 工場が稼働すると同時に、システムは 「outer 専用の
    #個室（ローカル名前空間①）」 をメモリ上に作成します。
print(data)
    #global
