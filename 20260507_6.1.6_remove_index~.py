#%% 6.1.6 複数要素を追加/置換/削除する（スライス構文を利用する）
data = ['あ','い','う','え','お',]
data[1:3] = ['1','2','3',]
    #[x:y]という書き方がスライス構文の書き方⇒[どこから：どこまで]注0始まり
'''
日本語で「1から3まで」と言うと「1、2、3」を指すことが多いですが、
Pythonの設計者はあえてこの「終了位置を含まない」仕様（英語で言うところの up to but not including）
を採用しました。
それには、論理的で美しい3つの理由があります。
 【なぜ？】 Python設計者の3つのこだわり
① インデックスは「箱」ではなく「境界線」を指しているというメンタルモデル
Python公式チュートリアルでは、「スライスの使い方をおぼえる良い方法は、
インデックスが文字と文字の『あいだ（境界線）』を指していると考えることだ」と明確に解説されています
。
例えば要素が並んでいるとき、インデックスの数字は要素の上ではなく、
**要素を仕切る「左端の線」**に書かれているとイメージしてください。
  +---+---+---+---+---+
  | あ| い| う | え| お|
  +---+---+---+---+---+
  0   1   2   3   4   5  <- ここがインデックス番号！
data[1:3] と指定した場合、「境界線 1 から境界線 3 の間にあるものを切り出せ」という命令になります。
図を見ると、境界線1から3の間にあるのは「い（インデックス1）」と「う（インデックス2）」の2つだけですよね
。これが終了位置を含まないように見える最大の理由です。
② スライスを分割・結合したときの「美しさ」
開始値が含まれ、終了値が含まれない仕様にすることで、
**「ある場所 i で真っ二つに割って、またくっつけると、必ず元のデータに戻る」**という
美しい法則が成り立ちます。

data[:2] （最初からインデックス2の直前まで） ➔ ['あ', 'い']
data[2:] （インデックス2から最後まで） ➔ ['う', 'え', 'お']
これらを足すと data[:2] + data[2:] == data となり、1つの要素も重複したり欠けたりすることなく元に戻ります
。もし終了位置を含んでいたら、「い」がダブってしまったりして計算がとても面倒になります。
③ 要素数の計算が「引き算」だけで済む
data[1:3] で切り出される要素の数は、単純に 3 - 1 = 2（個）になります
。 もし「終了位置を含む」仕様だった場合、要素数を求めるのに
(y - x) + 1 といちいち「+1」をしなければならず、エンジニアの計算ミス（オフバイワンエラー）を
誘発しやすくなります。
--------------------------------------------------------------------------------
■ 統合結論（英語圏のエンジニア的思考）
C言語などのプログラミング言語では、ループを回すときに for (int i = 0; i < N; i++) のように
「0からスタートして、Nの**未満（Nを含まない）**まで回す」という文化が古くから根付いています。
Pythonのスライス構文もこの「0オリジン（0始まり）」と「未満（not including）」の文化を色濃く受け継いでおり、
**「境界線でスパッと切り分ける」「引き算で要素数がわかる」**という合理性を優先して設計されました。
'''
print(data)
    #['あ', '1', '2', '3', 'え', 'お']
data[2:4] = []
print(data)
    #['あ', '1', 'え', 'お']
del data[2:4]
print(data)
    #['あ', '1']
'''
1. 【結論】 del の文法分類は「単純文（Simple statement）」
結論から申し上げますと、del は関数でもメソッドでもなく、
**Pythonの言語仕様に最初から組み込まれている「単純文（Simple statement）」**という文法に分類されます
。
関数： len(data) のようにカッコにデータを入れて渡すもの。
メソッド： data.pop() のようにオブジェクト自身に処理を命じるもの。
文（Statement）： if や for、return、pass、そして今回の del のように、
Pythonシステムそのものを制御するための「予約語」を使った命令です。
C言語でいうところの free() やC++の delete に近いイメージですが、
Pythonにおいて del は、「名前とデータの紐付け（束縛）を断ち切る」という役割を持つ強力な組み込み命令
として設計されています
。
'''
    
