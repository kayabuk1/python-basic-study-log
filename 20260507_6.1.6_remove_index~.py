# シャローコピーとディープコピー
import copy
    #import copy：より高度なコピー処理を行うための汎用的な道具箱（標準モジュール）
print(dir(copy))
data = [
        [1,2,3],
        [4,5,6],
        [7,8,9],
        ]
data2 = data.copy()
data[0][0] = 1000
print(data)
print(data2)
print(data[0][0] is data2[0][0]) #True
    #[[1000, 2, 3], [4, 5, 6], [7, 8, 9]]
    #[[1000, 2, 3], [4, 5, 6], [7, 8, 9]]
''' data2 = data.copy() で複製したはずなのに、
なぜか data = 1000 の変更が data2 にも波及
list.copy() は「浅いコピー（Shallow Copy）」しか作らない！
一番外側の箱（大枠）は新しく作りますが、
中に入っている「内側のリスト」までは新しく作ってくれないのです！
小箱の中身までコピーするのは面倒くさいから、
元の小箱に繋がる『糸（参照）』だけをコピーして大箱に入れておこう！'''
data = [
        [1,2,3],
        [4,5,6],
        [7,8,9],
        ]
data2 = copy.deepcopy(data)
data[0][0] = 1000
print(data)
print(data2)        
    #[[1000, 2, 3], [4, 5, 6], [7, 8, 9]]
    #[[1, 2, 3], [4, 5, 6], [7, 8, 9]]
print(data[2][2] is data2[2][2]) #True
print(data[0][0] is data2[0][0]) #False
'''ディープコピー（完全なるクローン）
おっ、今回は deepcopy（深いコピー）だな！ 大箱を新しく作るだけじゃない。
中に入っている小箱を見つけたら、それも新しく作り直す！
そのまた中に小箱があれば、それも作り直す！ 根こそぎ全てを完全にコピー（クローン）してやるぜ！
補足一番奥の数字 9 の is が True（同じオブジェクト）になっているのは、
Pythonが**「数値（int）や文字列（str）は変更不能（イミュータブル）なデータだから、
メモリの節約のために同じものを使い回しても絶対にバグらないよね」**という極限の最適化。
箱（リスト）は独立していますが、中に入っている「不変の数値そのもの」は使い回されている。
'''

