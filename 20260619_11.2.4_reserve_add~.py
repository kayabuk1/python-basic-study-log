#%% 11.2.5 オブジェクト同士を比較する 比較演算子のオーバーロード
class Coordinate:
    def __init__(self, x, y):
        self.x = x
        self.y = y
        
    #↓ < ルール
    def __lt__(self, other):
        return self.x**2 + self.y**2 \
            < other.x**2 + other.y**2
            #原点ー座標間の距離は三平方の定理から求められるので2乗している。
            #正しくはその平方根だが、比較なら途中でも問題ない。
    #↓ <= ルール
    def __le__(self, other):
        return self.x**2 + self.y**2 \
            <= other.x**2 + other.y**2
    #↓ > ルール
    def __gt__(self, other):
        return not self.__le__(other)
        #　> == not <=
        
    #↓ >= ルール
    def __ge__(self, other):
        return not self.__gt__(other)
        #>= == not <　になるので否定で求めるのがシンプルで良いとのこと
        #__ne__not equalも名前の通り__eq__の否定で十分。
    
    def __str__(self):
        return f'({self.x}{self.y})'
    
if __name__=='__main__'    :
    c1 = Coordinate(1, 2)
    c2 = Coordinate(15, 25)
    c3 = Coordinate(2, 1)
    print(c1 < c2) #True
    print(c1 <= c3) #True
    print(c1 <= c2) #True
    print(c1 > c2) #False
        
