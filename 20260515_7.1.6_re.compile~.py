#%% 7.1.6 正規表現途中から
# 名前付きキャプチャーグループの例
import re
# Regular Expression（正規表現）の略
#Python標準の、高度な文字列パターンマッチングを提供するモジュール。
print(dir(re))
msg = '仕事用はwings@exmple.comです。'
ptn = re.compile(
    r'(?i)(?P<localName>[a-z0-9.!#$%&\'*+/=?^\{|}~-]+)'
    r'@(?P<domain>[a-z0-9-]+(?:\.[a-z0-9-]+)*)'
    )
#① パターンの鋳造
#現場監督「おっ、メールアドレスの設計図だな！ (?i) は大文字小文字を無視しろという指示だ。
#そして (?P<localName>...) は、**『@の前の部分を見つけたら、
#そこを切り取って localName という名札を貼って保管（キャプチャ）しておけ』**という命令だな！
#同じく後ろには domain という名札を貼るぞ！」
#Q.なぜそういう意味になるの？詳しく解説して。
#A.Pythonが独自に用意した最強の機能が、名前付きキャプチャーグループ (?P<名前>...) です！
#（※ P は Python 特有の機能であることに由来します）
#現場監督「おい！ただ捕獲するだけじゃないぞ！ 括弧の最初にある ?P<localName> を見ろ！
#『捕獲したデータには、localName という名札をガムテープで貼り付けて保管しておけ！』という命令だ。
#(?i)：**Ignore case（大文字・小文字を無視しろ）**という現場監督への指示フラグです。
#(?P<localName> ... )：ここからここまでを捕獲して「localName」と名付ける。
#[a-z0-9.!#$%&\'*+/=?^\{|}~-]：[] は**「この括弧の中にある文字の『どれか1文字』」**という意味です
#（アルファベット、数字、そしてメールアドレスに使える大量の記号たち）。
#\.：ただのドット（. は「何でもいい1文字」という特殊記号なので、バックスラッシュでただのドットに戻しています）。
#(?: ... )：これは**「キャプチャ（捕獲）しないグループ化」**です。
#ただの () だとシステムが律儀に捕獲してメモリを使ってしまうため、
#「ただのまとまり（ブロック）を作りたいだけで、後から呼び出さないから捕獲しなくていいよ」というメモリ節約のテクニックです。
#（これで .com や .co.jp のような繰り返しに対応します）
#[] は、現場監督に対して「今から見せるリスト（カタログ）の中にある文字の『どれか1文字』を見つけてこい！」
#と指示するフィルター条件です文字クラス（Character class）」と呼びます。

#Q.そもそも re についても re.compile についても解っていないな。
#A.Compile（翻訳して機械語にする）」という言葉の通り、システムに対して
#「今から渡す正規表現の設計図を、何度も使い回せるように『専用の高速フィルター（金型）』としてメモリ上に鋳造しておけ！」
#と命令する機能です。 自動化スクリプトで10万行のログを処理する際、
#ループの中で毎回設計図を読み込ませると遅くなりますが、最初に re.compile で金型を作っておけば、処理速度が劇的に向上します。
#Q.正規表現の仕組みを分かりやすく覚える方法は無い？
#A. 正規表現とは「文字列の設計図（フィルター）」のことであり、
#覚えるコツは「英単語の暗記」ではなく「レゴブロックの組み合わせ」として捉えること。
'''
正規表現の「メンタルモデル」 すべてを暗記する必要は全くありません。
実務では以下の「4つのブロック」だけを意識し、あとは調べながら組み合わせるパズルだと考えてください。
1.文字の種類: \d (数字)、\w (英数字)、. (何でもいい1文字)
2.個数 (量指定子): + (1回以上)、* (0回以上)、? (あってもなくても良い)
3.場所: ^ (行頭)、$ (行末)
4.グループ化: () (囲んだ部分を1つのセットとして扱う ＝ キャプチャ)
'''
print(ptn.sub(r'\g<domain>の\g<localName>',msg))
#② 索敵と換装 (ptn.sub)
#現場監督「ターゲット msg の中に、金型に合う部分はあるか？
#あったぞ！ wings@exmple.com だ！ 名札を確認する！ localName は wings、domain は exmple.com だな！」
#現場監督「よし、見つけた部分を換装するぞ。指示書は \g<domain>の\g<localName> だから、
#保管した名札のパーツを逆に組み合わせて、exmple.comのwings に入れ替える！」
#出力結果: 仕事用はexmple.comのwingsです。

#Q.subもsubsutituteで置き換えだよね？f-stringで置き換えられるのになぜ.subメソッドを使う？
#A. f-string は「場所が分かっている空箱へのデータ注入」ですが、
#  .sub() は「どこにあるか分からない未知のターゲットを自律的に索敵して破壊・換装する自動追尾ミサイル」だからです！
#f-string の限界 (静的) f"{name}の気温は{temp}度です"
#  という書き方は、「ここに名前が入る」「ここに気温が入る」と、プログラマが文字列の構造をあらかじめ完全に把握している場合にしか使えません。
#re.sub() の真骨頂 (動的) 一方でインフラの実務では、
#「1万行のsyslogの中から、どこに書かれているか分からない 192.168.X.X というIPアドレスをすべて
#[MASKED_IP] に隠蔽しろ」といった要件が発生します。ここでは f-string は無力です。
#re.sub() を使えば、**「パターン（設計図）に合致するものをシステムが勝手に見つけ出し、一斉に別の文字列に置き換える」**ことができるのです。

#%% 7.1.7 正規表現で文字列を分割する splitメソッド
import re
msg = 'にわに３わうらにわに51わにわとりがいる'
ptn = re.compile(r'\d{1,}わ')
result = ptn.split(msg)
print(result)
#['にわに', 'うらにわに', 'にわとりがいる']
#Q.そもそもPatternクラスとは？？？
#A.高速フィルター（金型）」の、Pythonの世界における正式名称（データ型）が Pattern クラス（正規表現オブジェクト）
#現場監督（システム）「よし、渡された設計図 r'\d{1,}わ' を読み込んで、いつでも検索や分割ができる実体
#（オブジェクト）として組み立てたぞ！ この金型には Pattern という名前のクラス（設計図）を割り当てて、変数 ptn に代入しておこう！」
#Q.{1,}はどんな意味？
#A. 直前の文字が「1回以上（上限なし）繰り返される」という意味のレゴブロック（量指定子）です！ 実は、前回学んだ + と全く同じ働きをします！
#正規表現において、波括弧 {} は**「直前の文字の繰り返し回数を、人間が厳密にコントロールするためのダイヤル」**です。
'''
{min, max} ＝ 最小 min 回から、最大 max 回まで繰り返す。
{3} ＝ きっちり 3 回繰り返す。
{1,} ＝ 最小 1 回から、最大は指定なし（無限）まで繰り返す。
コードの設計図 r'\d{1,}わ' をレゴブロックに分解するとこうなります。
\d : 「数字（0〜9）」という文字の種類。
{1,} : それが「1回以上」続く。
わ : その直後に「わ」という文字が来る。
つまり、３わ（数字1桁）でも、51わ（数字2桁）でも、10000わ（数字5桁）でも、数字が何桁であろうと見つけ出せる最強のパターンの完成です。 
（※ちなみに、\d+わ と書いても全く同じ意味になります）
'''

