#%% 7.2.2 テキストファイルを読み込む
with open('access.log','r',encoding='UTF-8')as file:
    data = file.read()
    #data = file.read() （一括読み込み）
    #現場監督「ファイルの中身を全て1つの巨大な文字列にして持ち帰ってきたぞ！」
    #※手軽ですが、10GBのアクセスログでこれをやるとメモリがパンク（OOMキル）します。
print(data)

#行単位にファイルを取得する readlinesメソッド
with open('access.log','r',encoding='UTF-8')as file:
    data = file.readlines()
    #data = file.readlines() （リスト化読み込み）
    # 現場監督「1行ごとに切り分けて、**巨大な『リスト』**にして持ち帰ってきたぞ！」
    # ※これも結局メモリに全て載せるため、巨大ファイルには不向きです。
for line in data:
    print(line,end='')
print(dir(file))
print(dir(file.read))
