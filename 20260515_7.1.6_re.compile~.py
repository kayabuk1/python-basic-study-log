# fileオブジェクトをforでループする
with open('access.log','r',encoding='UTF-8') as file:
    #Q.なぜopenに引き渡すパラメータは1、2文字なのに''で囲う？
    #A.1文字であっても '' で囲まないと、システムが「文字列（データ）」ではなく「変数（名札）」だと勘違いしてパニックを起こすからです。
    for line in file:
        print(line,end='')
    #for line in file:（イテレータによるストリーム処理）
    #これがインフラエンジニアの黄金パターンです！
    #現場監督「ファイル全体は読み込まない！1行だけディスクからメモリに読み込んで
    #変数 line に渡し、処理が終わったらそのメモリを捨てて、次の1行を取りに行くぞ！」
    #この仕組み（イテレータ）のおかげで、どれだけ巨大なログファイルであっても、メモリをほとんど消費せずに高速に処理できるのです。

#　シーク位置を変更する seekメソッド ※seek(バイト数)
#Q.seekは実務的にはどんな所で役に立つのだろうか？
#A.次回起動時に file.seek(前回の位置) を実行すれば、「ファイルを開いて一瞬で前回の続きの場所にワープし、
#  そこから増えた差分だけを読み込む」**という超高速なログ監視（Linuxの tail -f コマンドと同じ動き）がPythonで自作できる。
with open('access.log','r',encoding='UTF-8',
          newline='\n') as file:
    file.seek(6)
    for line in file:
        print(line,end='')

