#%%# 4.4 例外処理
#構文エラーと例外
#下のコードに山田と入れられると、、、
num = input('数字を入力してください：')
print('2倍すると',float(num)*2)
    #ValueError: could not convert string to float: '山田'
    #処理が止まってしまう。
#try...exceptで書き直すと
try:
    num = input('数字を入力してください:')
    print('2倍すると...', float(num)*2)
except ValueError as ex:
    print('エラー発生：', ex)
    #エラー発生： could not convert string to float: '山田'
'''
■ ex とは何か？
一言で言うと、**「発生したエラーの詳細情報が詰まった箱（例外インスタンス）」**です。
システムがエラー（例外）を検知した瞬間、単に処理を止めるだけでなく、
その原因やメッセージをこの箱（オブジェクト）の中にパッキングして、私たち（プログラマ）に手渡してくれます。
■ ex の特徴と機能
証拠の保持: 単に「ValueError（値のエラー）が起きた」という種類だけでなく、
「具体的にどんな文字が入ってきて失敗したのか（今回の場合は '山田' ）」という詳細なエラーメッセージを保持しています。
自動整形機能: print(ex) のようにそのまま表示させようとすると、箱の中の複雑なデータから
**「人間が読みやすいエラーメッセージ（ could not convert string to float: '山田' ）」だけを
自動的に抽出してテキストにしてくれる**という、非常に便利な仕様になっています
。

'''
#%% 4.4.2 例外が発生した場合、しなかった場合の処理を記述する
while True:
    try:
        num = input('数字を入力してください:')
        print('2倍すると．．．',float(num*2))
    except ValueError:
        print('入力値エラーです。')
    else:
        break
'''
数字を入力してください:あ
入力値エラーです。
数字を入力してください:１
2倍すると．．． 11.0

num*2 の計算: Pythonの公式仕様として、文字列に対して *（アスタリスク）を使うと、
掛け算ではなく**「文字列の反復（繰り返し）」**として処理されます。 
つまり、 '1' * 2 が実行され、結果は '11' という文字列になりました。
float() の変換: 最後に、一番外側にある float() が、
完成した '11' という文字列を実数に変換しました。その結果、 11.0 となって出力されたのです。
全角の １ を入力した場合でも、Pythonの float() は賢く数値を認識して変換するため、 
'１１' が見事に 11.0 として解釈され、この見事な「11.0バグ」が完成

🔶教訓：計算する前に、まず型を数値に変換（キャスト）する」 この鉄則と、文字列に対する * 演算子の動作理解
'''
