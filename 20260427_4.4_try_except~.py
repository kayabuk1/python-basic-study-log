'''関数の 2*2マトリクス（引数と戻り値）
                 |戻り値なし      |戻り値あり
                 |（結果を返さない）|（結果を返す）
-------------------------------------------|
引数なし          |１．決まった動作　|3．常に何らかの|
（データを受け取らない）|　　だけを行う   |　データを生成|
------------------------------------------|
引数あり　　　　  |2．収受データに　|4．収受データを |
（データを受け取る）　|　処理だけ行う  |　加工して返す|
-----------------------------------------|
'''
#パターン1：引数なし＊戻り値なし
#決まった挨拶を表示するなど、外部からの情報も不要で、結果を返す必要もない
#（その場で完結する）関数。
def say_hello():
    #入力も出力もなく、 ただ画面に出力して終わる。
    print("こんにちは！！")
#呼び出し
say_hello()
    #こんにちは！！

#パターン2：引数あり＊戻り値なし
#データを受け取って処理（画面表示やファイル書き込みなど）を行いますが、
#計算結果などを、呼び出し元に、返さない関数。
def greet_user(name):
    #名前(引数)を受け取って表示するが、戻り値はない。
    print(F'{name}さん、こんにちは！')
greet_user('横須賀')
    #横須賀さん、こんにちは！

##パターン3：引数なし＊戻り値あり    
#外部からのデータは不要ですが、呼びされる度に何らかのデータ（乱数、現在時刻、固定の設定値など）
#を生成して返す関数。
import random
def get_luckuy_number():
    #引数は無いが、計算した乱数を戻り値として返す。
    number = random.randint(1, 100)
    return number
#呼び出し（戻り値を変数で受け取る）
result = get_luckuy_number()
print(f'今日のラッキーナンバーは{result}です。')
    #今日のラッキーナンバーは86です。

#パターン4：引数あり＊戻り値あり（最も標準的）
#材料（引数）を受け取り、それを元に計算や加工を行い、その結果（戻り値）を返す関数。
#数学の y = f(x)と同じ概念。⇒つまり値に出力が一対一対応する？
def calculate_area(base, height):
    #底辺と高さ（引数）を受け取り、面積を計算して返す（戻り値）
    area = (base*height)/2
    return area
#呼び出し（引数を渡し、戻り値を変数で受け取る）
triangle_area = calculate_area(10, 5)
print(F'三角形の面積は {triangle_area} です！')
    #三角形の面積は 25.0 です！
