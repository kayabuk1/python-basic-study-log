# tarnslateメソッドの書き方2パターン
# ①辞書型を使う方法：str.maketrans(dict) ※○○変換前:××変換後で記載
# ②引数に全て書く方法：str.maketrans(old変換前, new変換後[, no削除文字])
'''
str.maketrans() ＝ 「変換ルールブック（対応表）」を作る専門家
.translate() ＝ ルールブックを読み込んで「実際に文字を変換する」実行役
'''
msg = '今日の、あなたの運勢は、●良い感じ●です'
print(msg.translate(str.maketrans({
    ',':'、',
    '.':'。',
    '●':''
    })))
print(msg.translate(str.maketrans(',.','、。','●')))

#%% 5.2.11 文字を整形する（formatメソッド）
# txt書式文字列.formatメソッド(*args, **kwargs)
'''
主語（左側）： '{}は{},{}歳です。' これは、途中に {} という「空っぽの穴
        （プレースホルダ）」が空いた状態の、ただの文字列オブジェクトです。
動詞（右側）： .format('サクラ', '女の子', 2) その文字列オブジェクトに対して、
            **「お前の中に空いている {} の穴に、今から渡す材料（引数）を
            順番にハメ込んで、完成品の文字列を作れ！」**という命令
            （メソッド）を出しています。

システムは裏側で、主語である文字列をスキャンし、{} を見つけるたびに
引数（サクラ、女の子、2）を文字列表現（ str() ）に変換しながら
流し込んでいます。
教科書に書かれている (*args, **kwargs) は、
前回学んだ [, count] と同じく、説明書用の記号です。
公式ドキュメントでは「可変長引数」と呼ばれる機能を示しています。
*args（任意個数の位置引数）： 「カンマ区切りで、いくつでも好きなだけ材料
                            を渡していいよ」という意味です。今回のように
                            3つ渡せば、左から順に {} に入ります。
**kwargs（任意個数のキーワード引数）： 「名前（キーワード）をつけて材料を
                                    渡してもいいよ」という意味です。
                                    
実は format の {} の中には、インデックス番号や名前（キーワード）をつける
ことができます。
# キーワード引数（**kwargs）を使った、インフラ実務でよくやる書き方
print('{name}は{gender},{age}歳です。'
      .format(name='サクラ', gender='女の子', age=2))

f-string は、.format() の長ったらしい構文を極限まで圧縮した「超・省略構文（シンタックスシュガー）」であり、裏側でやっているメモリ上の処理は今回の .format() と本質的に同じです。
'''

print('{}は{},{}歳です。'.format('サクラ', '女の子', 2))
    #サクラは女の子,2歳です。
    #C言語では printf("%sは%d歳です", name, age);
print('{0}は{2}歳,{1}です。'.format('サクラ','女の子',2))
print('{name}は{age}歳,{sex}です。'.format(
    name = 'サクラ',sex = '女の子',age = 2))
name = 'サクラ'
sex = '女の子'
age = 2
print(F'{name}は{age}歳、{sex}です。')
print(f"{name=}は{age=}歳、{sex=}です")
    #サクラは2歳、女の子です。
    #name='サクラ'はage=2歳、sex='女の子'です

#%% 書式指定子
'''
　　　　　　[indexno]:[[fill]align][sign][#][0][width][group_opt][.prec][type]
[[桁不足埋文字]配置指定][符号][#][0埋][最小幅][桁区切り][有効桁]｢ﾃﾞｰﾀ型｣

■書式指定子を分解する。
「[:]→書式指定子に於いては、ここから先はデータそのものでなく、
    表示形式を整える為の[書式指定子（ｵﾌﾟｼｮﾝ）]という目印                  
{0}→パーサー[よし！0番目の引数をそのまま入れればよいだな]
{0:*>10}→パーサー[:が来たぞ。と言うことは、:の左側の(0)が対象データで、
        　:右側の(*>10)はそいつをどう見せるかのﾙｰﾙﾌﾞｯｸ（書式指定子）だな]
■[[fill]align][sign][#][width][group_opt][.prec][type]
 からどうしてシステムは迷わない？→[使用可能記号種類]と[並び順]が厳格に決まっている。
■ パーサーの脳内シミュレーション（ *>10 の場合）
1．アラインメント（配置）の検索 パーサーはまず、配置を決める専用記号（ < > = ^ ）を
　探します。 ➔ 「あ、 > があるぞ！ ということは、align（配置指定）は >（右寄せ） だ」
2．フィル（穴埋め文字）の特定 配置記号が見つかった場合、パーサーは「その直前にある1文字
　を必ずフィル文字とみなすルールを持っています。 ➔ 「 > の直前には * があるな。
　よし、fill（桁不足時の埋め文字）は * だ」
3．符号・その他のフラグの検索 ➔ 「 + や - （sign）、 # は無いな。飛ばそう」
4．幅（width）の特定 ➔ 「 > の後ろに 10 という数字の連続があるぞ。
　これは width（最小幅）＝10 だ」
5．小数点やデータ型の検索 ➔ 「 . （精度）もないし、最後に f や d のような
　アルファベット（データ型）もないな。じゃあ型の指定は省略（デフォルト）だな」 

= がトリッキーな理由： ネットワークやサーバーのログ画面で、マイナス符号の位置を綺麗に揃えるために作られた「数値専用の特殊な配置記号」だから。
パターンA：理詰めで書いた場合（昌敏さんのコード）
{0:0=10} ➔ パーサー：「align が = で、その左にある fill が 0 だな。よし、マイナス符号の後に 0 を埋めよう」 （※これは文法規則に100%忠実な、最も厳格で正しい書き方です）
パターンB：横着して書いた場合（C言語の歴史的書き方）
{0:010} ➔ パーサー：「おや？ align 記号（<>=^）がないぞ？でも独立した ``（0埋めフラグ） が指定されて、幅が 10 だな。よし、自動的に `fill='0'`、`align='='` と指定されたものとして強制的に解釈しよう！」
 なぜこんな「被り」があるのか？
前回の解説で「C言語の printf では %010d と書く」とお話ししたのを覚えていますでしょうか。 何十年もの間、世界中のエンジニアは「ゼロ埋めといえば 010 と書く」という習慣（手癖）が染み付いていました。
Pythonの設計者は、新しく論理的な [[fill]align] というルール（つまり 0=）を作ったものの、昔からのエンジニアが「いつもの %010d みたいに書きたい！」と文句を言わないように、**「align を省略して単に 010 と書いても、裏側で勝手に 0=10 に翻訳してあげるよ」**というショートカット機能（ `` フラグ）を残したのです。
                 
'''
print('{0:>10}'.format('wings'))
    #     wings
    #0番目の文字を代入。右詰めalign記号の左には何もないのでfillは空白
    #符号もその後ろに0も無い、10は最小幅指定だな。
    #結果→10文字分空白確保して右詰めで0番目文字wingsを配置する。
print( '{0:*>10}'.format('wings'))
    #*****wings
    #0番目の文字を代入。右詰めaling記号の左に*なのでfill=*
    #符号もその後ろに0も無い。10は最小幅指定。
    #結果→10文字分確保してwingsを右詰め配置して*埋め。
print('{0:0=10}'.format(-12345))
    #-000012345
    #0番目の文字を代入。align記号は[=]fillは0
    #符号は空白なので0番目
    #文字に依る。10文字分確保。まず-12345を入れる。
    #[=]なので符号マイナスの後ろに0000を挿入
print('{0:,}'.format(9876543210))
    #9,876,543,210
print('{0:#_x}'.format(0x5f5bce1aa))
    #0x5_f5bc_e1aa












